import dotenv from 'dotenv';
import mongoose from 'mongoose';
import Appointment from '../models/Appointment.js';

dotenv.config();

async function updateOperationalStatusOnly() {
  try {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('‚úÖ Conectado ao MongoDB');

    const statusMap = {
      'agendado': 'agendado',
      'confirmado': 'confirmado',
      'cancelado': 'cancelado',
      'conclu√≠do': 'pago',
      'faltou': 'cancelado'
    };

    const appointments = await Appointment.find({ status: { $in: Object.keys(statusMap) } });
    console.log(`üîç Encontrados ${appointments.length} agendamentos com status v√°lidos.`);

    let atualizados = 0;

    for (const [i, appt] of appointments.entries()) {
      const newStatus = statusMap[appt.status];
      if (appt.operationalStatus !== newStatus) {
        appt.operationalStatus = newStatus;
        await appt.save();
        console.log(`‚úÖ ${i + 1}: ${appt._id} - "${appt.status}" ‚Üí "${newStatus}"`);
        atualizados++;
      }
    }

    console.log(`\nüéâ Atualiza√ß√£o conclu√≠da. Total atualizados: ${atualizados}`);
    process.exit(0);
  } catch (err) {
    console.error('‚ùå Erro durante atualiza√ß√£o:', err);
    process.exit(1);
  }
}

updateOperationalStatusOnly().catch(console.error);
